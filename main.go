package main

import (
	"fmt"
	"time"

	particles "github.com/heitorcaldeira/coffee-with-prime/pkg"
)

var cup = `
                    .:-----====----------------:.                     
                 .:=-===++--:::-===========+=------:                  
                ::==+===-==:::::-:.:--:--:::--===----:                
               .:++===:..--:::::::::::..--....:-==--+.:               
               .=:=+==-::.::::::::::::::-:.....:===-:-=::....:        
                :.:--=+==--::::...........:::-==+=-----. .... :.      
              :-=   :::---==+++==------==++==---:::..=..:   .: -      
          .-=-:.-        .....::--------::.....   ...=.+-:  :: -      
        .=-:.....-                                ..=.=..:+-. :.      
       -=:.......:-                              ..-::----. :=        
      -=........  .-                            ..-:  .  ::-.=-       
      -=......      :.                          :-.::::::.....-:      
      --:....        .:.                      :-.::.     ....:-:      
       ---..           ==:                  :=-          ...:--       
        .---.           .-=-::.        .::-=-.          ..:--:        
          -==-:.           .::--======--::.            ::-=-          
            :-=-=-::.                             .::-=-=-            
               ::-=:----::.......     .......::-----=-:.              
                   .::::--:::::---------:::::--::::.                  
                          ...::::::::::::....                        `

func main() {
  coffee := particles.NewCoffee(71, 20)
  coffee.Start()

  timer := time.NewTicker(100 * time.Millisecond)
  for {
    <- timer.C
    fmt.Print("\033[H\033[2J")
    coffee.Update()
    fmt.Println(coffee.Display())
    fmt.Println(cup)
  }
}
